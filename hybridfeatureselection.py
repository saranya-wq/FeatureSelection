from sklearn.metrics import jaccard_score

def subset_stability(subsets):

    scores = []

    for i in range(len(subsets)):
        for j in range(i+1,len(subsets)):

            s = jaccard_score(subsets[i],subsets[j])
            scores.append(s)

    return np.mean(scores),np.std(scores)

