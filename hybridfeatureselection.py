class ECALayer(nn.Module):

    def __init__(self, channels, k_size=9):
        super().__init__()
        self.avg_pool = nn.AdaptiveAvgPool1d(1)
        self.conv = nn.Conv1d(1,1,kernel_size=k_size,padding=(k_size-1)//2,bias=False)

    def forward(self,x):
        y = x.unsqueeze(1)
        y = self.avg_pool(y)
        y = self.conv(y)
        y = torch.sigmoid(y)
        return x * y.squeeze(1)


