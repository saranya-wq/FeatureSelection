def stability_analysis(feature_runs):

    sims=[]

    for i in range(len(feature_runs)):

        for j in range(i+1,len(feature_runs)):

            sims.append(
                jaccard_similarity(
                    set(feature_runs[i]),
                    set(feature_runs[j])
                )
            )

    return {

        "Avg Jaccard":np.mean(sims),
        "Std Dev":np.std(sims),
        "Min":np.min(sims),
        "Max":np.max(sims)
    }

